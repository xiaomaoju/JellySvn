// Current language (defaults to 'en')
let _currentLanguage = 'en';

/**
 * Get translated string for the given key.
 * Falls back to English, then to the key itself.
 */
function t(key, params) {
    const lang = _currentLanguage;
    let text = (translations[lang] && translations[lang][key]) ||
               translations['en'][key] ||
               key;

    // Simple parameter substitution: t('msg.confirmMove', { source: 'a', target: 'b' })
    // Use replaceAll so a template like "Delete {name}? Confirm {name}" substitutes
    // every occurrence, and pass a function replacement so values containing $
    // (e.g. "$100", "$1") aren't interpreted as backreferences.
    if (params) {
        for (const [k, v] of Object.entries(params)) {
            const needle = `{${k}}`;
            const value = String(v);
            if (text.includes(needle)) {
                text = text.split(needle).join(value);
            }
        }
    }

    return text;
}

/**
 * Set the current language and re-render the UI.
 */
function setLanguage(lang) {
    if (!translations[lang]) {
        console.warn(`Language '${lang}' not supported. Falling back to 'en'.`);
        lang = 'en';
    }
    _currentLanguage = lang;
    state.settings.language = lang;
    saveSettings();

    // Keep <html lang="..."> in sync for screen readers / locale-aware APIs
    if (typeof document !== 'undefined' && document.documentElement) {
        document.documentElement.lang = lang;
    }

    // Re-render sidebar, static HTML, and current view
    renderSidebar();
    localizeStaticHTML();
    switchView(state.currentView);
}

/**
 * Get the current language code.
 */
function getCurrentLanguage() {
    return _currentLanguage;
}

/**
 * Initialize language from settings (called during app init).
 */
function initLanguage(lang) {
    if (lang && translations[lang]) {
        _currentLanguage = lang;
    }
    if (typeof document !== 'undefined' && document.documentElement) {
        document.documentElement.lang = _currentLanguage;
    }
}
